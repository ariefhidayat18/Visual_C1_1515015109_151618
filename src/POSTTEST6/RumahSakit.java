    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String nama_pasien,tl,jk = null,diagnosa,dokter,ruangan,tgl;
        int usia;
        nama_pasien = txtNama.getText();
        tl = txtTtl.getText();
        usia = Integer.parseInt(txtUsia.getText());
        if(rbLaki.isSelected()){
            jk = rbLaki.getText();
        }
        else if(rbPerempuan.isSelected()){
            jk = rbPerempuan.getText();
        }
        diagnosa = lstDiagnosa.getSelectedValue().toString();
        dokter = txtDokter.getText();
        ruangan = txtRuang.getText();
        tgl = txtTgl.getText();
        TambahData(nama_pasien, tl, usia, jk, diagnosa, dokter, ruangan, tgl);

        txtDokter.setText("");
        txtNama.setText("");
        txtRuang.setText("");
        txtTgl.setText("");
        txtTtl.setText("");
        txtUsia.setText("");
        buttonGroup1.clearSelection();
        btnTambah.setEnabled(true);
    }//GEN-LAST:event_btnSimpanActionPerformed

